#Tipos de operadores e precedência

produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2)    #adição
print(produto_1 - produto_2)    #subtração
print(produto_1 / produto_2)    #divisão com ponto flutuante (divisão exata)
print(produto_1 // produto_2)   #divisão inteira (arredonda sempre para baixo)
print(produto_1 * produto_2)    #multiplicação
print(produto_1 % produto_2)    #resto da divisão
print(produto_1 ** produto_2)   #exponenciação

x = (10 + 5) * 4
y = (10 / 2) + (25 * 2) - (2 ** 2)
print(x)
print(y)